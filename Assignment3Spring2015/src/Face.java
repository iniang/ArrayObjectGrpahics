public class Face {
	   
		    
	int x;
    int y;
    int height;
    int width;

	public Face(int xval, int yval, int widthval, int heightval) {
		 x=xval;
		 y=yval;
		 height=heightval;
		 width=widthval;
	}

	public int getY() {
		
		return y;
	}

	public int getX() {
		
		return x;
	}

	public int getWidth() {
		
		return width;
	}

	public int getHeight() {
	
		return height;
	}

}
